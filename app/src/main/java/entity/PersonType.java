public enum PersonType {
    RUNNER, EMPLOYEE, MANAGER, ADMIN;

    PersonType() {}

    public PersonType getType() {
        return this;
    }

    @Override
    public String toString() {
        String s = "RUNNER";
        switch (this) {
            case RUNNER:
                s = "RUNNER";
                break;
            case EMPLOYEE:
                s = "EMPLOYEE";
                break;
            case MANAGER:
                s = "MANAGER";
                break;
            case ADMIN:
                s = "ADMIN";
                break;
        }
        return s;
    }
}
